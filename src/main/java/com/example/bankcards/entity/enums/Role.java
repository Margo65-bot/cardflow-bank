package com.example.bankcards.entity.enums;

/**
 * Роль пользователя в системе.
 *
 * <p>Определяет уровень доступа:</p>
 * <ul>
 *   <li>{@code USER} — обычный пользователь: может управлять своими картами,
 *       совершать переводы, просматривать историю</li>
 *   <li>{@code ADMIN} — администратор: имеет доступ ко всем картам, пользователям,
 *       транзакциям, может блокировать/удалять карты, менять роли</li>
 * </ul>
 *
 * <p>В Spring Security роль преобразуется в {@code ROLE_USER} или {@code ROLE_ADMIN}.</p>
 */
public enum Role {
    USER,
    ADMIN
}
